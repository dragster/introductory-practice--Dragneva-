# =====================================================================
# 1. РЕЗУЛЬТАТ ПЕРИОДА (Прибыль / Убыток / Безубыточность)
# =====================================================================
print("--- 1. Результат периода ---")
# Запрашиваем прибыль (или убыток, если ввести отрицательное число) за месяц
monthly_profit = float(input("Введите финансовый результат за месяц (руб.): "))

if monthly_profit > 0:
    print("Прибыль")
elif monthly_profit < 0:
    print("Убыток")
else:
    print("Безубыточность")
print()


# =====================================================================
# 2. КАТЕГОРИЯ БИЗНЕСА (По размеру выручки)
# =====================================================================
print("--- 2. Категория бизнеса ---")
revenue = float(input("Введите годовую выручку бизнеса (руб.): "))

if revenue <= 1_000_000:
    print("Микробизнес")
elif revenue <= 10_000_000:
    print("Малый")
elif revenue <= 100_000_000:
    print("Средний")
else:
    print("Крупный")
print()


# =====================================================================
# 3. РАСЧЁТ НДФЛ И СУММЫ «НА РУКИ»
# =====================================================================
print("--- 3. Расчёт НДФЛ ---")
salary = float(input("Введите размер заработной платы (руб.): "))

# Определение налоговой ставки в зависимости от размера зарплаты
if salary <= 50_000:
    tax_rate = 0.13
else:
    tax_rate = 0.15

tax_amount = salary * tax_rate
take_home_pay = salary - tax_amount

print(f"Налог (НДФЛ): {tax_amount:,.2f} руб.")
print(f"Сумма «на руки»: {take_home_pay:,.2f} руб.")
print()


# =====================================================================
# 4. РОСТ ВКЛАДА ПО ГОДАМ (Сложный процент на 5 лет)
# =====================================================================
print("--- 4. Рост вклада по годам ---")
initial_capital = float(input("Введите начальный капитал (руб.): "))
annual_interest_rate = float(input("Введите годовую процентную ставку (%): "))

# Переводим проценты из формата (например, 10) в коэффициент (0.10)
interest_coefficient = annual_interest_rate / 100
current_balance = initial_capital

# Цикл рассчитывает сложный процент на 5 лет вперед
for year in range(1, 6):
    current_balance += current_balance * interest_coefficient
    print(f"Год {year}: сумма на счёте — {current_balance:,.2f} руб.")
print()


# =====================================================================
# 5. НАКОПИТЕЛЬНЫЙ СЧЁТ (Ежемесячные регулярные взносы)
# =====================================================================
print("--- 5. Накопительный счёт ---")
monthly_deposit = float(input("Введите сумму ежемесячного взноса (руб.): "))
number_of_months = int(input("Введите количество месяцев для накопления: "))

total_savings = 0.0

# Цикл по каждому месяцу для симуляции регулярных накоплений
for month in range(1, number_of_months + 1):
    total_savings += monthly_deposit
    print(f"Месяц {month}: накоплено — {total_savings:,.2f} руб.")

