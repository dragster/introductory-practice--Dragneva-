product_2_name = input("Введите название 2-го товара: ")
product_2_price = float(input("Введите цену 2-го товара: "))

product_3_name = input("Введите название 3-го товара: ")
product_3_price = float(input("Введите цену 3-го товара: "))

# Вывод сформированного прайс-листа в заданном формате
print("\nНаш прайс-лист:")
print("Товар:", product_1_name, "—", product_1_price, "руб.")
print("Товар:", product_2_name, "—", product_2_price, "руб.")
print("Товар:", product_3_name, "—", product_3_price, "руб.")
print()


# =====================================================================
# 6. СЧЁТ С НДС
# =====================================================================
print("--- Расчет счета с НДС ---")
# Запрос исходных данных у пользователя
quantity = int(input("Введите количество товара: "))
price_per_item = float(input("Введите цену за единицу товара: "))
vat_rate = float(input("Введите ставку НДС (%): "))

# Математические расчеты
total_without_vat = quantity * price_per_item
vat_amount = total_without_vat * (vat_rate / 100)
total_with_vat = total_without_vat + vat_amount

# Вывод детализированного счета
print("\nДетализация счета:")
print("Стоимость без НДС:", total_without_vat, "руб.")
print("Сумма НДС:", vat_amount, "руб.")
print("Итого к оплате (с НДС):", total_with_vat, "руб.")

