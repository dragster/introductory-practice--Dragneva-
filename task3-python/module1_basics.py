## Модуль 1. Переменные, типы данных, ввод/вывод 



# =====================================================================
# 1. КАРТОЧКА СОТРУДНИКА
# =====================================================================
# Создание переменных разных типов данных
employee_name = "Дарья"
employee_age = 19
employee_salary = 250500.50
is_currently_employed = True

# Вывод данных с поясняющими подписями
print("--- Карточка сотрудника ---")
print("Имя сотрудника:", employee_name)
print("Возраст:", employee_age, "лет")
print("Текущая зарплата:", employee_salary, "руб.")
print("Статус (работает ли сейчас):", is_currently_employed)
print()  # Пустая строка для разделения заданий в консоли


# =====================================================================
# 2. СТОИМОСТЬ ЗАКАЗА
# =====================================================================
print("--- Расчет стоимости заказа ---")
# Запрос данных у пользователя с преобразованием типов
item_price = float(input("Введите цену товара: "))
item_quantity = int(input("Введите количество товара: "))

# Вычисление итоговой стоимости
total_order_cost = item_price * item_quantity

# Вывод результата
print("Итоговая стоимость вашего заказа:", total_order_cost, "руб.")
print()


# =====================================================================
# 3. ДОХОД ПО ВКЛАДУ
# =====================================================================
print("--- Расчет дохода по вкладу ---")
# Запрос параметров вклада
deposit_amount = float(input("Введите сумму вклада (руб.): "))
interest_rate = float(input("Введите годовую ставку (%): "))

# Вычисление чистого дохода за один год
annual_profit = deposit_amount * (interest_rate / 100)

# Вывод результата с пояснением
print("За 1 год ваш чистый доход по вкладу составит:", annual_profit, "руб.")
print()


# =====================================================================
# 4. ИЗМЕНЕНИЕ ЦЕНЫ АКЦИИ
# =====================================================================
print("--- Анализ изменения цены акции ---")
# Запрос начальной и конечной стоимости
initial_stock_price = float(input("Введите начальную цену акции: "))
final_stock_price = float(input("Введите конечную цену акции: "))

# Расчет абсолютного и процентного изменения
absolute_change = final_stock_price - initial_stock_price
percentage_change = (absolute_change / initial_stock_price) * 100

# Вывод результатов анализа
print("Абсолютное изменение цены:", absolute_change, "руб.")
print("Процентное изменение:", percentage_change, "%")
print()


# =====================================================================
# 5. МИНИ-ПРАЙС-ЛИСТ
# =====================================================================
print("--- Создание мини-прайс-листа ---")
# Запрос названий и цен для трех разных товаров
product_1_name = input("Введите название 1-го товара: ")
